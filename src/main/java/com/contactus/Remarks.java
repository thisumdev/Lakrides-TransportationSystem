package com.contactus;

public class Remarks {
	private String username;
	private String remark;
	
	public Remarks(String username, String remark) {
		this.username = username;
		this.remark = remark;
	}

	public String getUsername() {
		return username;
	}


	public String getRemark() {
		return remark;
	}


	
	
}
